import type { NextPage } from "next";
import Head from "next/head";
import { trpc } from "../utils/trpc";
import {useRouter} from "next/router";
import {useCookies} from "react-cookie";
import {useEffect, useRef, useState} from "react";

const Home: NextPage = () => {
    const router = useRouter();

    const { mutate } = trpc.useMutation("lobby.create", {
        onSuccess: (data) => {
            router.push(`/lobby/${data.lobbyCode}`);
        },
    });

    const nameElement = useRef<HTMLInputElement>(null);
    const [cookies, setCookie] = useCookies(['name']);
    const name = useRef<string>('');

    useEffect(() => {
        name.current = cookies.name
    }, [])

    return (
    <>
        <Head>
            <title>Create T3 App</title>
                <meta name="description" content="Generated by create-t3-app" />
            <link rel="icon" href="/favicon.ico" />
        </Head>

        <div>
            <form onSubmit={(e) => {
                e.preventDefault()
                setCookie('name', nameElement.current?.value)
            }}>
                <input ref={nameElement}
                       type="text"
                       placeholder={name.current || "Type name"}
                       className="input input-bordered w-full max-w-xs"/>
            </form>
        </div>

        <div className="btn-group">
            <button className="btn btn-primary" onClick={() => {mutate()}}>Create</button>
            <button className="btn">Join</button>
        </div>
    </>
  );
};

export default Home;
